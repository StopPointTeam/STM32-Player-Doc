int main(void)
{
	Cache_Enable();											//打开L1-Cache
	HAL_Init();													//初始化HAL库
	Stm32_Clock_Init(160,5,2,4);				//设置时钟,400Mhz 
	delay_init(400);										//延时初始化
	uart_init(115200);									//串口初始化
	LED_Init();													//初始化LED时钟  
	KEY_Init();
	IWDG_Init(IWDG_PRESCALER_64,500); 	//分频数为64,重载值为500,溢出时间为1s	
	LED2(0);                       		 	//先点亮LED2
	
	while(1)
	{
		if(KEY_Scan(0)==KEY2_PRES)  	   //如果K2按下，喂狗
		{
			IWDG_Feed();    		          	//喂狗
		}
		delay_ms(10);      
	}
}



































